let numero = 45;  

if (numero >= 0 && numero <= 10) {
    console.log("El número está entre 0 y 10.");
} else if (numero >= 11 && numero <= 20) {
    console.log("El número está entre 11 y 20.");
} else if (numero >= 21 && numero <= 30) {
    console.log("El número está entre 21 y 30.");
} else if (numero >= 31 && numero <= 40) {
    console.log("El número está entre 31 y 40.");
} else if (numero >= 41 && numero <= 50) {
    console.log("El número está entre 41 y 50.");
} else {
    console.log("El número está fuera de los rangos especificados.");
}
